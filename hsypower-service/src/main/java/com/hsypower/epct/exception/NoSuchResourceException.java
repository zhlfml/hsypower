package com.hsypower.epct.exception;

public class NoSuchResourceException extends HsypowerException {

	private static final long serialVersionUID = 3940658485595578618L;

	public NoSuchResourceException() {
		super();
	}

	public NoSuchResourceException(String message, Throwable cause) {
		super(message, cause);
	}

	public NoSuchResourceException(String message) {
		super(message);
	}

	public NoSuchResourceException(Throwable cause) {
		super(cause);
	}

	
}
